#!/usr/bin/env python3
"""
Plot energy/token and performance/W from a power summary CSV.

Expected input format is the CSV generated by:
  scripts/results/power_summary_T1_T4.csv

This version averages Prefill + Decode throughput for each (threads, mode),
then derives:
  energy/token = power / avg_throughput
  perf/W       = avg_throughput / power
"""

from __future__ import annotations

import argparse
import csv
from collections import defaultdict
from pathlib import Path

import matplotlib.pyplot as plt


def thread_to_int(thread_label: str) -> int:
    # Supports labels like "T1", "t4", or plain "4"
    s = thread_label.strip().upper()
    if s.startswith("T"):
        s = s[1:]
    return int(s)


def load_rows(csv_path: Path):
    rows = []
    with csv_path.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        required = {"threads", "mode", "phase", "throughput_tokens_per_s", "power_w"}
        missing = required - set(reader.fieldnames or [])
        if missing:
            raise ValueError(f"Missing CSV columns: {sorted(missing)}")

        for r in reader:
            rows.append(
                {
                    "thread": thread_to_int(r["threads"]),
                    "mode": r["mode"].strip().upper(),
                    "phase": r["phase"].strip().capitalize(),
                    "throughput": float(r["throughput_tokens_per_s"]),
                    "power_w": float(r["power_w"]),
                }
            )
    return rows


def build_series(rows):
    # Aggregate Prefill + Decode into one average throughput per (thread, mode).
    # data[mode] -> list[(thread, avg_tps, energy, perf_w)]
    by_tm = defaultdict(lambda: {"throughputs": [], "power_vals": []})
    for r in rows:
        key = (r["thread"], r["mode"])
        by_tm[key]["throughputs"].append(r["throughput"])
        by_tm[key]["power_vals"].append(r["power_w"])

    data = defaultdict(list)
    for (thread, mode), agg in by_tm.items():
        if not agg["throughputs"]:
            continue
        avg_tps = sum(agg["throughputs"]) / len(agg["throughputs"])
        # Power should be the same across phases; use mean for robustness.
        p = sum(agg["power_vals"]) / len(agg["power_vals"])
        energy = p / avg_tps
        perf_w = avg_tps / p
        data[mode].append((thread, avg_tps, energy, perf_w))

    for mode in data:
        data[mode].sort(key=lambda x: x[0])
    return data


def plot(data, output_path: Path, title: str):
    styles = {
        "CPU": {"color": "#1f77b4", "linestyle": "-", "marker": "o"},
        "FPGA": {"color": "#d62728", "linestyle": "-", "marker": "o"},
    }

    fig, axes = plt.subplots(1, 2, figsize=(12, 4.5))
    ax_energy, ax_perf = axes

    for mode, vals in sorted(data.items()):
        threads = [v[0] for v in vals]
        energy = [v[2] for v in vals]
        perf_w = [v[3] for v in vals]
        style = styles.get(mode, {"linestyle": "-", "marker": "o"})
        label = mode

        ax_energy.plot(threads, energy, label=label, **style)
        ax_perf.plot(threads, perf_w, label=label, **style)

    ax_energy.set_title("Energy per token")
    ax_energy.set_xlabel("Threads")
    ax_energy.set_ylabel("J/token")
    ax_energy.grid(True, alpha=0.3)

    ax_perf.set_title("Performance per watt")
    ax_perf.set_xlabel("Threads")
    ax_perf.set_ylabel("tokens/s/W")
    ax_perf.grid(True, alpha=0.3)

    # Single legend for both plots (kept separate from title to avoid overlap)
    handles, labels = ax_energy.get_legend_handles_labels()
    fig.suptitle(title, y=0.98)
    fig.legend(handles, labels, loc="upper center", bbox_to_anchor=(0.5, 0.93), ncol=2, frameon=False)
    fig.tight_layout(rect=(0, 0, 1, 0.86))

    output_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(output_path, dpi=200, bbox_inches="tight")
    print(f"Saved plot: {output_path}")


def main():
    ap = argparse.ArgumentParser(
        description="Plot energy/token and performance/W from summary CSV."
    )
    ap.add_argument(
        "--input",
        type=Path,
        default=Path("scripts/results/power_summary_T1_T4.csv"),
        help="Input CSV path",
    )
    ap.add_argument(
        "--output",
        type=Path,
        default=Path("scripts/results/power_summary_T1_T4.png"),
        help="Output PNG path",
    )
    ap.add_argument(
        "--title",
        type=str,
        default="CPU vs FPGA efficiency across thread counts",
        help="Figure title",
    )
    args = ap.parse_args()

    rows = load_rows(args.input)
    data = build_series(rows)
    plot(data, args.output, args.title)


if __name__ == "__main__":
    main()
