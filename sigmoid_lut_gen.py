#!/usr/bin/env python3
"""
Generate sigmoid_lut.h for the swiglu Vitis HLS project.
Run once before C synthesis:
    python3 sigmoid_lut_gen.py

Produces sigmoid_lut.h containing:
  - A C simulation branch that computes values at runtime using expf()
  - A synthesis branch with 4096 precomputed float values as a ROM array

LUT parameters:
  Entries : 4096
  Range   : x in [-8.0, +8.0]
  Step    : 16 / 4096 = 0.00390625
  Index   : idx = clamp((int)((z + 8.0f) * 256.0f), 0, 4095)
  Value   : sigmoid(x[idx]) = 1 / (1 + exp(-x[idx]))
"""
import math

N    = 4096
lo   = -8.0
step = 16.0 / N

vals = [1.0 / (1.0 + math.exp(-(lo + i * step))) for i in range(N)]

with open("sigmoid_lut.h", "w") as f:
    f.write("// sigmoid_lut.h -- AUTO-GENERATED by sigmoid_lut_gen.py. Do not edit.\n")
    f.write("// sigmoid(x) sampled at 4096 points over [-8, +8], step = 16/4096.\n")
    f.write("// Index formula: idx = clamp((int)((z + 8.0f) * 256.0f), 0, 4095)\n")
    f.write("#ifndef SIGMOID_LUT_H\n")
    f.write("#define SIGMOID_LUT_H\n\n")

    # C simulation branch: computed at runtime to avoid a large generated file
    f.write("#ifndef __SYNTHESIS__\n")
    f.write("#include <math.h>\n")
    f.write("static float sigmoid_lut[4096];\n")
    f.write("static inline void init_sigmoid_lut_csim(void) {\n")
    f.write("    static int done = 0;\n")
    f.write("    if (done) return;\n")
    f.write("    for (int i = 0; i < 4096; i++) {\n")
    f.write("        float x = -8.0f + (float)i * (16.0f / 4096.0f);\n")
    f.write("        sigmoid_lut[i] = 1.0f / (1.0f + expf(-x));\n")
    f.write("    }\n")
    f.write("    done = 1;\n")
    f.write("}\n")
    f.write("#else\n")

    # Synthesis branch: precomputed ROM
    f.write("// Precomputed ROM for synthesis (BRAM via BIND_STORAGE in swiglu.cpp).\n")
    f.write("static const float sigmoid_lut[4096] = {\n")
    row = []
    for i, v in enumerate(vals):
        row.append(f"{v:.9f}f")
        if len(row) == 8:
            f.write("    " + ", ".join(row) + ",\n")
            row = []
    if row:
        f.write("    " + ", ".join(row) + "\n")
    f.write("};\n")
    f.write("#endif // __SYNTHESIS__\n\n")
    f.write("#endif // SIGMOID_LUT_H\n")

print(f"Generated sigmoid_lut.h with {N} sigmoid values.")
print(f"  x[0]    = {lo:.4f}  -> sigmoid = {vals[0]:.9f}")
print(f"  x[2048] =  0.0000  -> sigmoid = {vals[2048]:.9f}")
print(f"  x[4095] = {lo + 4095*step:.4f}  -> sigmoid = {vals[4095]:.9f}")
